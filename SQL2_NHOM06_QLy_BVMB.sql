CREATE TRIGGER TRG_CHECK_TONGTIEN
ON THANHTOAN
AFTER INSERT
AS
BEGIN
    UPDATE THANHTOAN
    SET TongTien =
    (
        SELECT GIAVE.Gia +
               ISNULL(
                    (SELECT HANHLY.PhiThem
                     FROM HANHLY
                     WHERE HANHLY.MaVe = VECHUYENBAY.MaVe), 0
