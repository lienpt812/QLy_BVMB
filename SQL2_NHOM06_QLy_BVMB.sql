            SELECT *
            FROM GHE
            WHERE MaGhe = @MaGhe
              AND MaMayBay = @MaMayBay
        )
        BEGIN
            PRINT N'Ghế không tồn tại trên máy bay này';
            ROLLBACK TRAN;
