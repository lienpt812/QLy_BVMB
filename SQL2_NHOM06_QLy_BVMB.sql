        END

        IF EXISTS
        (
            SELECT *
            FROM VECHUYENBAY
            WHERE MaCB = @MaCB
              AND MaGhe = @MaGhe
        )
        BEGIN
            PRINT N'Ghế đã được đặt';
            ROLLBACK TRAN;
            RETURN;
