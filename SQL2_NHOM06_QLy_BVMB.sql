        VALUES (@MaThanhToan, @MaVe, @PhuongThucThanhToan, 0);

        EXEC SP_TINHTONGTIEN;

        SELECT @SoGhe = SoLuongGhe
        FROM MAYBAY
        WHERE MaMayBay = @MaMayBay;

        SELECT @SoGheDaDat = COUNT(*)
        FROM VECHUYENBAY
        WHERE MaCB = @MaCB;

        IF @SoGheDaDat >= @SoGhe
        BEGIN
            UPDATE CHUYENBAY
            SET TrangThai = N'Hết vé'
            WHERE MaCB = @MaCB;
        END

        COMMIT TRAN;
    END TRY
    BEGIN CATCH
        IF @@TRANCOUNT > 0
            ROLLBACK TRAN;
        PRINT N'Đặt vé thất bại';
    END CATCH
END;

