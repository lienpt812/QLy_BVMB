                CASE
                    WHEN @KhoiLuongHanhLy BETWEEN 25 AND 29 THEN 100000
                    WHEN @KhoiLuongHanhLy = 30 THEN 200000
                    WHEN @KhoiLuongHanhLy BETWEEN 31 AND 34 THEN 300000
                    WHEN @KhoiLuongHanhLy = 35 THEN 400000
                    ELSE 0
